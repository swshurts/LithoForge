def _emit_flat_top_bottom_faces(top, cell_ok, ti, bi, faces):
    """Emit the top + bottom triangle list for flat-mode slabs using
    greedy rectangle merging on the top surface (lossless) and on the
    bottom (always a single flat rect group)."""
    z_top_vert = top[..., 2]
    for (r0, c0, r1, c1, is_flat) in _greedy_top_rects(z_top_vert, cell_ok):
        if is_flat:
            a, b = ti(r0, c0), ti(r0, c1 + 1)
            c, d = ti(r1 + 1, c1 + 1), ti(r1 + 1, c0)
        else:
            a, b = ti(r0, c0), ti(r0, c0 + 1)
            c, d = ti(r0 + 1, c0 + 1), ti(r0 + 1, c0)
        faces.append((a, b, c))
        faces.append((a, c, d))
    for (r0, c0, r1, c1) in _bottom_rects(cell_ok):
        ba, bb = bi(r0, c0), bi(r0, c1 + 1)
        bc, bd = bi(r1 + 1, c1 + 1), bi(r1 + 1, c0)
        faces.append((ba, bc, bb))
        faces.append((ba, bd, bc))


def _emit_curved_top_bottom_faces(h, w, cell_ok, ti, bi, faces):
    """Per-pixel top + bottom for curved / disc slabs. Greedy meshing
    is unsafe here because each pixel's z varies along a curve."""
    for y in range(h - 1):
        for x in range(w - 1):
            if not cell_ok[y, x]:
                continue
            a, b = ti(y, x), ti(y, x + 1)
            c, d = ti(y + 1, x + 1), ti(y + 1, x)
            faces.append((a, b, c))
            faces.append((a, c, d))
            ba, bb = bi(y, x), bi(y, x + 1)
            bc, bd = bi(y + 1, x + 1), bi(y + 1, x)
            faces.append((ba, bc, bb))
            faces.append((ba, bd, bc))


def _emit_perimeter_walls(h, w, cell_ok, ti, bi, faces):
    """External slab walls — wherever a cell's neighbour is invalid (or
    we're at the image boundary), emit one quad bridging top→bottom
    along that cell edge."""
    def add_wall(t0_idx, t1_idx, b0_idx, b1_idx):
        faces.append((t0_idx, b0_idx, b1_idx))
        faces.append((t0_idx, b1_idx, t1_idx))

    for y in range(h - 1):
        for x in range(w - 1):
            if not cell_ok[y, x]:
                continue
            if y == 0 or not cell_ok[y - 1, x]:
                add_wall(ti(y, x + 1), ti(y, x), bi(y, x + 1), bi(y, x))
            if y == h - 2 or not cell_ok[y + 1, x]:
                add_wall(ti(y + 1, x), ti(y + 1, x + 1),
                         bi(y + 1, x), bi(y + 1, x + 1))
            if x == 0 or not cell_ok[y, x - 1]:
                add_wall(ti(y, x), ti(y + 1, x), bi(y, x), bi(y + 1, x))
            if x == w - 2 or not cell_ok[y, x + 1]:
                add_wall(ti(y + 1, x + 1), ti(y, x + 1),
                         bi(y + 1, x + 1), bi(y, x + 1))


def _emit_step_walls(h, w, top, cell_ok, half_layer_mm, ti, faces):
    """Internal step walls — when two horizontally or vertically
    adjacent cells have different top z's (by more than half a layer),
    emit two triangles closing the vertical gap so the topmost slab
    reproduces the lithophane silhouette."""
    # Step walls along x (column transitions).
    for y in range(h - 1):
        for x in range(w - 2):
            if not cell_ok[y, x] or not cell_ok[y, x + 1]:
                continue
            z_left = top[y, x + 1, 2]
            z_right = top[y, x + 2, 2]
            if abs(z_left - z_right) <= half_layer_mm:
                continue
            t0, t1 = ti(y, x + 1), ti(y + 1, x + 1)
            if z_left > z_right:
                faces.append((t0, t1, ti(y + 1, x + 2)))
                faces.append((t0, ti(y + 1, x + 2), ti(y, x + 2)))
            else:
                faces.append((t0, ti(y, x + 2), ti(y + 1, x + 2)))
                faces.append((t0, ti(y + 1, x + 2), t1))
    # Step walls along y (row transitions).
    for y in range(h - 2):
        for x in range(w - 1):
            if not cell_ok[y, x] or not cell_ok[y + 1, x]:
                continue
            z_up = top[y + 1, x, 2]
            z_dn = top[y + 2, x, 2]
            if abs(z_up - z_dn) <= half_layer_mm:
                continue
            t0, t1 = ti(y + 1, x), ti(y + 1, x + 1)
            if z_up > z_dn:
                faces.append((t0, ti(y + 2, x + 1), t1))
                faces.append((t0, ti(y + 2, x), ti(y + 2, x + 1)))
            else:
                faces.append((t0, t1, ti(y + 2, x + 1)))
                faces.append((t0, ti(y + 2, x + 1), ti(y + 2, x)))


def _build_slab_mesh(
    clipped_layers: np.ndarray,
    z_floor_mm: float,
    layer_height_mm: float,
    geo: GeometrySpec,
) -> Tuple[np.ndarray, np.ndarray]:
    """Watertight slab mesh: each pixel rises `clipped_layers[r,c]`
    layer-heights above `z_floor_mm`. Pixels where `clipped_layers == 0`
    are absent. Used by the per-color 3MF writer so every filament
    gets its own watertight `<object>`.

    Output triangles split across four helpers — see each one's docstring:
        - top/bottom (flat-mode greedy OR curved per-pixel)
        - perimeter walls (where a slab footprint ends)
        - step walls (between adjacent valid pixels with differing z's)
    """
    h, w = clipped_layers.shape

    # Per-pixel "synthetic layer_map" we can pass through _build_vertices.
    # Pixels where this filament is absent get z=0 too (masked out anyway).
    synthetic = clipped_layers.astype(np.float64)
    top, bottom = _build_vertices(synthetic, layer_height_mm, geo)
    top[..., 2] += z_floor_mm
    bottom[..., 2] = z_floor_mm

    valid = clipped_layers > 0
    cell_ok = (
        valid[:-1, :-1] & valid[:-1, 1:] & valid[1:, :-1] & valid[1:, 1:]
    )
    if geo.mode == "disc":
        disc_valid = _disc_valid_mask(h, w, geo)
        cell_ok &= (
            disc_valid[:-1, :-1] & disc_valid[:-1, 1:]
            & disc_valid[1:, :-1] & disc_valid[1:, 1:]
        )

    verts_top = top.reshape(-1, 3)
    verts_bot = bottom.reshape(-1, 3)
    vertices = np.concatenate([verts_top, verts_bot], axis=0)
    top_offset = 0
    bot_offset = verts_top.shape[0]

    def ti(y, x):
        return top_offset + y * w + x

    def bi(y, x):
        return bot_offset + y * w + x

    faces: List[Tuple[int, int, int]] = []

    if geo.mode == "flat":
        _emit_flat_top_bottom_faces(top, cell_ok, ti, bi, faces)
    else:
        _emit_curved_top_bottom_faces(h, w, cell_ok, ti, bi, faces)

    _emit_perimeter_walls(h, w, cell_ok, ti, bi, faces)
    _emit_step_walls(h, w, top, cell_ok, layer_height_mm * 0.5, ti, faces)

    faces_arr = (
        np.array(faces, dtype=np.int32)
        if faces else np.zeros((0, 3), dtype=np.int32)
    )
    return vertices, faces_arr


def build_per_filament_slabs(
    layer_map: np.ndarray,
    swap_layer_indices: List[int],
    n_filaments: int,
    layer_height_mm: float,
    geo: GeometrySpec,
    base_min_layers: int = 1,
) -> List[Tuple[int, np.ndarray, np.ndarray]]:
    """Return [(filament_idx, vertices, faces), ...] — one tuple per
    filament that actually contributes geometry. Filament order matches
    the input filaments list (the print's swap order).

    For filament k:
      bottoms[k] = (cumulative layer index where filament k begins)
      tops   [k] = (next swap layer index OR layer_map.max()+1 for last)
      clipped[r,c] = max(0, min(layer_map[r,c], tops[k]) - bottoms[k])

    `base_min_layers` (>= 1) forces the BASE filament (slot 0) to fill
    every in-domain pixel with at least that many layers, so the 3MF
    has no zero-thickness voids. Voids are still possible in disc mode
    *outside* the inscribed circle (those pixels are intentionally
    excluded by the geometry mask).
    """
    if n_filaments <= 0:
        return []
    bottoms = [0] + list(swap_layer_indices)
    top_cap = int(np.max(layer_map)) + 1
    tops = list(swap_layer_indices) + [top_cap]
    # Pad / truncate so we have exactly n_filaments slabs
    while len(bottoms) < n_filaments:
        bottoms.append(top_cap)
    while len(tops) < n_filaments:
        tops.append(top_cap)
    bottoms = bottoms[:n_filaments]
    tops = tops[:n_filaments]

    # Bump the working layer map so the BASE slab's clip math fills
    # voids with `base_min_layers`. We deliberately only bump the floor
    # — pixels with layer_map > floor keep their original allocation.
    floor = max(1, int(base_min_layers))
    base_layer_map = np.maximum(layer_map, floor)

    slabs: List[Tuple[int, np.ndarray, np.ndarray]] = []
    for k in range(n_filaments):
        if tops[k] <= bottoms[k]:
            continue
        source = base_layer_map if k == 0 else layer_map
        clipped = np.clip(
            source - bottoms[k], 0, tops[k] - bottoms[k]
        ).astype(np.int32)
        if int((clipped > 0).sum()) == 0:
            continue
        verts, faces = _build_slab_mesh(
            clipped, bottoms[k] * layer_height_mm, layer_height_mm, geo,
        )
        if faces.shape[0] == 0:
            continue
        slabs.append((k, verts, faces))
    return slabs


def _compact_mesh(
    vertices: np.ndarray,
    faces: np.ndarray,
) -> Tuple[np.ndarray, np.ndarray]:
    """Drop any vertex not referenced by `faces` and re-index. Greedy
    meshing leaves hundreds of thousands of unused per-pixel vertices
    in the grid — without this pass the 3MF would still be 5-10× larger
    than necessary because every unused vertex still serialises as
    `<vertex x=… y=… z=…/>` in the XML."""
    if faces.shape[0] == 0:
        return np.zeros((0, 3), dtype=vertices.dtype), faces
    used = np.unique(faces.reshape(-1))
    # Build the new-index lookup: a sparse map from old vertex id → new.
    remap = np.full(vertices.shape[0], -1, dtype=np.int32)
    remap[used] = np.arange(used.shape[0], dtype=np.int32)
    new_vertices = vertices[used]
    new_faces = remap[faces]
    return new_vertices, new_faces


def _mesh_to_3mf_object(
    obj_id: int,
    vertices: np.ndarray,
    faces: np.ndarray,
    filament_idx: int,
    filament_name: str,
    filament_hex: str,
) -> str:
    """A single 3MF `<object>` tagged with the source filament slot,
    name and RGB hex. Uses standard `<metadatagroup>` (core spec) so
    ANY 3MF reader — including ForgeSlicer — can recover the color
    without needing a vendor extension."""
    # Drop unreferenced vertices before serialising — greedy meshing
    # leaves most of the per-pixel vertex grid unused; without this
    # pass the 3MF can be 5-10× larger than necessary.
    vertices, faces = _compact_mesh(vertices, faces)
    parts: List[str] = [
        f'<object id="{obj_id}" type="model" name="{_xml_escape(filament_name)}">',
        '<metadatagroup>',
        f'<metadata name="lithoforge:filament_slot">{filament_idx}</metadata>',
        f'<metadata name="lithoforge:filament_name">{_xml_escape(filament_name)}</metadata>',
        f'<metadata name="lithoforge:filament_hex">{filament_hex.upper()}</metadata>',
        '</metadatagroup>',
        '<mesh><vertices>',
    ]
    for v in vertices:
        parts.append(f'<vertex x="{v[0]:.4f}" y="{v[1]:.4f}" z="{v[2]:.4f}"/>')
    parts.append('</vertices><triangles>')
    for f in faces:
        parts.append(f'<triangle v1="{f[0]}" v2="{f[1]}" v3="{f[2]}"/>')
    parts.append('</triangles></mesh></object>')
    return "".join(parts)


def _xml_escape(s: str) -> str:
    return (
        s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
        .replace('"', "&quot;").replace("'", "&apos;")
    )


def _color_3mf_model(
    slabs: List[Tuple[int, np.ndarray, np.ndarray]],
    filaments: List[Tuple[str, str]],
) -> str:
    """Multi-object 3MF model XML. Each filament that contributes
    geometry becomes an `<object>` tagged with its slot and hex; all
    objects share a single `<build>` so the slicer treats them as one
    print.
    """
    parts: List[str] = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<model unit="millimeter" xml:lang="en-US" ',
        'xmlns="http://schemas.microsoft.com/3dmanufacturing/core/2015/02">',
        '<metadata name="Application">LithoForge</metadata>',
        '<resources>',
    ]
    obj_id = 1
    ids_in_build: List[Tuple[int, int]] = []
    for filament_idx, verts, faces in slabs:
        name, hex_ = filaments[filament_idx]
        parts.append(_mesh_to_3mf_object(
            obj_id, verts, faces, filament_idx, name, hex_,
        ))
        ids_in_build.append((obj_id, filament_idx))
        obj_id += 1
    parts.append('</resources>')
    parts.append('<build>')
    for oid, slot in ids_in_build:
        # `transform` is identity — slabs are already positioned in mm.
        # Bambu Studio / OrcaSlicer / PrusaSlicer all honour a
        # `printable="1"` attribute on items; we leave it default-true.
        parts.append(
            f'<item objectid="{oid}" transform="1 0 0 0 1 0 0 0 1 0 0 0"/>'
        )
    parts.append('</build>')
    parts.append('</model>')
    return "".join(parts)


def _model_settings_config_color(
    slabs: List[Tuple[int, np.ndarray, np.ndarray]],
) -> str:
    """Bambu/Orca convention: per-object `extruder` mapping in
    `Metadata/model_settings.config`. Object ids start at 1 to match
    `_color_3mf_model`. Bambu uses 1-indexed extruder numbers, so
    filament slot 0 maps to extruder 1, slot 1 → 2, etc."""
    rows: List[str] = []
    obj_id = 1
    for filament_idx, *_ in slabs:
        rows.append(
            f'    <object id="{obj_id}">\n'
            f'      <metadata key="extruder" value="{filament_idx + 1}"/>\n'
            f'    </object>'
        )
        obj_id += 1
    return (
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<config>\n'
        + "\n".join(rows)
        + '\n</config>\n'
    )


