_CONTENT_TYPES = (
    '<?xml version="1.0" encoding="UTF-8"?>'
    '<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">'
    '<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>'
    '<Default Extension="model" ContentType="application/vnd.ms-package.3dmanufacturing-3dmodel+xml"/>'
    '<Default Extension="config" ContentType="application/vnd.ms-printing.3dmanufacturing-ct-colorcfg+xml"/>'
    '</Types>'
)

_RELS = (
    '<?xml version="1.0" encoding="UTF-8"?>'
    '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">'
    '<Relationship Target="/3D/3dmodel.model" Id="rel0" Type="http://schemas.microsoft.com/3dmanufacturing/2013/01/3dmodel"/>'
    '</Relationships>'
)


def _mesh_to_3mf_model(vertices: np.ndarray, faces: np.ndarray) -> str:
    # Drop unreferenced vertices before serialising (see _compact_mesh).
    vertices, faces = _compact_mesh(vertices, faces)
    parts: List[str] = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<model unit="millimeter" xml:lang="en-US" ',
        'xmlns="http://schemas.microsoft.com/3dmanufacturing/core/2015/02">',
        '<resources>',
        '<object id="1" type="model"><mesh><vertices>',
    ]
    for v in vertices:
        parts.append(f'<vertex x="{v[0]:.4f}" y="{v[1]:.4f}" z="{v[2]:.4f}"/>')
    parts.append('</vertices><triangles>')
    for f in faces:
        parts.append(f'<triangle v1="{f[0]}" v2="{f[1]}" v3="{f[2]}"/>')
    parts.append('</triangles></mesh></object>')
    parts.append('</resources>')
    parts.append(
        '<build><item objectid="1" transform="1 0 0 0 1 0 0 0 1 0 0 0"/></build>'
    )
    parts.append('</model>')
    return "".join(parts)


def _project_config(
    filaments: List[Tuple[str, str]],
    swap_heights_mm: List[float],
    layer_height_mm: float,
    profile: PrinterProfile,
    license_text: str = "",
) -> str:
    """Bambu/Orca/Prusa-compatible project settings subset.

    Embeds `layer_change_gcode` with the appropriate per-printer flavour:
    `M600` pauses for single-extruder, `T<n>` tool changes for AMS / MMU /
    multi-tool printers. Also tags the project with the printer's name
    and the creator-declared license.
    """
    colors = [c for _, c in filaments]
    names = [n for n, _ in filaments]

    swap_layer_indices: List[int] = []
    for z in swap_heights_mm[1:]:
        swap_layer_indices.append(max(1, int(round(z / layer_height_mm))))
    layer_change_gcode = build_layer_change_gcode(profile, swap_layer_indices)

    cfg: dict[str, Any] = {
        "filament_colour": colors,
        "filament_type": ["PLA"] * len(colors),
        "filament_settings_id": names,
        "layer_height": layer_height_mm,

        # Per-printer auto-swap G-code (M600 vs T<n>).
        "before_layer_change_gcode": layer_change_gcode,
        "layer_change_gcode": layer_change_gcode,

        # Printer + bed metadata so slicers pick the right machine on import.
        "printer_model": profile["name"],
        "printer_manufacturer": profile["manufacturer"],
        "slicer_family": profile["slicer_family"],
        "printable_area": [
            "0x0",
            f"{profile['bed_x_mm']}x0",
            f"{profile['bed_x_mm']}x{profile['bed_y_mm']}",
            f"0x{profile['bed_y_mm']}",
        ],
        "printable_height": profile["max_z_mm"],

        # Reference data for tooling that reads this file.
        "cmykw_swap_heights_mm": swap_heights_mm,
        "cmykw_swap_colors": colors,
        "cmykw_swap_layers": swap_layer_indices,
        "cmykw_multi_tool": profile["multi_tool"],
    }
    if license_text:
        cfg["cmykw_license"] = license_text
    return json.dumps(cfg, indent=2)


def write_3mf(
    vertices: np.ndarray,
    faces: np.ndarray,
    filaments: List[Tuple[str, str]],
    swap_heights_mm: List[float],
    layer_height_mm: float,
    profile: Optional[PrinterProfile] = None,
    license_text: str = "",
    per_filament_slabs: Optional[
        List[Tuple[int, np.ndarray, np.ndarray]]
    ] = None,
) -> bytes:
    """Build a 3MF container with the mesh + color metadata.

    If `per_filament_slabs` is provided the model XML emits one
    `<object>` per filament (each tagged with `lithoforge:filament_slot`,
    `:filament_hex`, `:filament_name` metadata + a Bambu-style
    `extruder` value in `Metadata/model_settings.config`). This is the
    representation downstream tools — ForgeSlicer in particular —
    consume to recover per-color regions without re-deriving them from
    the Z-band layer history.

    Falling back to a single merged mesh (`per_filament_slabs is None`)
    is supported for STL parity / legacy callers.
    """
    if profile is None:
        profile = get_profile("generic_orca")
    if per_filament_slabs:
        model_xml = _color_3mf_model(per_filament_slabs, filaments)
    else:
        model_xml = _mesh_to_3mf_model(vertices, faces)
    project_cfg = _project_config(
        filaments, swap_heights_mm, layer_height_mm, profile, license_text
    )

    buf = io.BytesIO()
    with zipfile.ZipFile(buf, mode="w", compression=zipfile.ZIP_DEFLATED) as z:
        z.writestr("[Content_Types].xml", _CONTENT_TYPES)
        z.writestr("_rels/.rels", _RELS)
        z.writestr("3D/3dmodel.model", model_xml)
        z.writestr("Metadata/project_settings.config", project_cfg)
        if per_filament_slabs:
            z.writestr(
                "Metadata/model_settings.config",
                _model_settings_config_color(per_filament_slabs),
            )
    return buf.getvalue()


