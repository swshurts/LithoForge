"""Export layer maps as STL + color-swap instructions or as a 3MF bundle.

The STL is a heightmap mesh generated by marching over the layer_map and
producing two triangles per cell for the top surface plus a flat bottom
and four side walls. For flat geometry this is straightforward; curved
("arched") geometry wraps the heightmap around a cylinder section.

3MF output is a simplified Bambu/Prusa-style bundle: the STL mesh embedded
in the 3MF's OPC container along with a `Metadata/project_settings.config`
and `Metadata/model_settings.config` that declare the filament colors and
swap layers. It is readable by modern slicers as a single object with
per-layer color hints.
"""

from __future__ import annotations

import io
import json
import struct
import zipfile
from dataclasses import dataclass
from typing import Any, List, Optional, Tuple

import numpy as np

from printers import PrinterProfile, build_layer_change_gcode, get_profile


@dataclass
class GeometrySpec:
    width_mm: float
    height_mm: float
    border_mm: float = 0.0
    mode: str = "flat"          # "flat" | "curved" | "cylindrical" | "disc"
    curve_radius_mm: float = 80  # used when mode == curved/cylindrical
    dome_mm: float = 0.0         # used when mode == disc (gentle dome height)


# ---------------------------------------------------------------------------
# Mesh building
# ---------------------------------------------------------------------------


def _greedy_top_rects(
    z_top: np.ndarray,
    valid_cells: np.ndarray,
) -> List[Tuple[int, int, int, int, bool]]:
    """Decompose a cell grid into maximal rectangles where all 4 corner
    vertex z's are identical. Lossless mesh decimation — fewer triangles,
    same geometry to the slicer.

    Args:
        z_top: (H+1, W+1) per-vertex z values (mm).
        valid_cells: (H, W) bool — True where the cell should be meshed.

    Returns:
        List of (r0, c0, r1, c1, flat) tuples — inclusive cell ranges.
        `flat=True` means the rect can emit ONE quad (2 triangles) using
        the 4 outer corners. `flat=False` is a singleton cell that still
        has unequal corners and must emit its 2 triangles using its own
        4 corner vertices.

    Notes on T-junctions: greedy rectangle merging can leave a vertex on
    the interior of an adjacent rectangle's edge. Slicers (Bambu Studio /
    OrcaSlicer / PrusaSlicer / Cura) handle this gracefully because they
    slice via 2D plane intersections — the T-junction does not affect the
    sliced contours. For pure rendering use cases T-junctions can show
    hairline cracks; we accept that tradeoff because the target audience
    is 3D printing slicers, not real-time rendering.
    """
    H, W = valid_cells.shape
    if H == 0 or W == 0:
        return []

    # Per-cell: do all 4 corner vertices share the same z?
    z00 = z_top[:-1, :-1]
    z01 = z_top[:-1, 1:]
    z10 = z_top[1:, :-1]
    z11 = z_top[1:, 1:]
    flat = (z00 == z01) & (z00 == z10) & (z00 == z11)

    visited = np.zeros((H, W), dtype=bool)
    rects: List[Tuple[int, int, int, int, bool]] = []

    for r in range(H):
        c = 0
        while c < W:
            if visited[r, c] or not valid_cells[r, c]:
                c += 1
                continue
            if not flat[r, c]:
                # Cell whose corners disagree — emit as a singleton; no
                # merging possible without distorting the geometry.
                visited[r, c] = True
                rects.append((r, c, r, c, False))
                c += 1
                continue
            z = float(z00[r, c])
            # Extend rightward while cells are valid, flat, same-z, unvisited.
            c_end = c
            while c_end + 1 < W:
                nxt = c_end + 1
                if (
                    visited[r, nxt]
                    or not valid_cells[r, nxt]
                    or not flat[r, nxt]
                    or float(z00[r, nxt]) != z
                ):
                    break
                c_end += 1
            # Extend downward; every cell in [c..c_end] must match.
            r_end = r
            while r_end + 1 < H:
                row_slice = slice(c, c_end + 1)
                rr = r_end + 1
                if (
                    visited[rr, row_slice].any()
                    or not valid_cells[rr, row_slice].all()
                    or not flat[rr, row_slice].all()
                    or not (z00[rr, row_slice] == z).all()
                ):
                    break
                r_end += 1
            visited[r:r_end + 1, c:c_end + 1] = True
            rects.append((r, c, r_end, c_end, True))
            c = c_end + 1

    return rects


def _bottom_rects(valid_cells: np.ndarray) -> List[Tuple[int, int, int, int]]:
    """Decompose `valid_cells` into maximal rectangles (used for the
    flat slab bottom — all bottom z's are equal so we just need to
    cover the footprint with as few quads as possible)."""
    H, W = valid_cells.shape
    if H == 0 or W == 0:
        return []
    visited = np.zeros((H, W), dtype=bool)
    rects: List[Tuple[int, int, int, int]] = []
    for r in range(H):
        c = 0
        while c < W:
            if visited[r, c] or not valid_cells[r, c]:
                c += 1
                continue
            c_end = c
            while c_end + 1 < W and not visited[r, c_end + 1] and valid_cells[r, c_end + 1]:
                c_end += 1
            r_end = r
            while r_end + 1 < H:
                rr = r_end + 1
                row_slice = slice(c, c_end + 1)
                if visited[rr, row_slice].any() or not valid_cells[rr, row_slice].all():
                    break
                r_end += 1
            visited[r:r_end + 1, c:c_end + 1] = True
            rects.append((r, c, r_end, c_end))
            c = c_end + 1
    return rects


def _build_vertices(
    layer_map: np.ndarray,
    layer_height_mm: float,
    geo: GeometrySpec,
    base_min_layers: int = 1,
) -> Tuple[np.ndarray, np.ndarray]:
    """Return (top_vertices, bottom_vertices) shaped (H, W, 3).

    `base_min_layers` enforces a minimum solid floor under every pixel
    so the heightmap never has zero-thickness holes (which the slicer
    treats as voids — undesirable for lithophanes that are meant to be
    continuous prints). Default 1 = original behavior; 2..5 thickens
    the void-fill base.
    """
    h, w = layer_map.shape
    usable_w = geo.width_mm - 2 * geo.border_mm
    usable_h = geo.height_mm - 2 * geo.border_mm

    xs = np.linspace(geo.border_mm, geo.border_mm + usable_w, w)
    ys = np.linspace(geo.border_mm, geo.border_mm + usable_h, h)

    # Enforce a minimum floor of `base_min_layers` so the print stays
    # continuous — slicers otherwise treat zero-height pixels as voids.
    floor = max(1, int(base_min_layers))
    z_top = (np.maximum(layer_map, floor) * layer_height_mm).astype(np.float64)

    if geo.mode in ("flat", "disc"):
        X, Y = np.meshgrid(xs, ys)
        # Disc gets an optional gentle dome bump added to z_top.
        if geo.mode == "disc" and geo.dome_mm > 0:
            cx = geo.border_mm + usable_w / 2.0
            cy = geo.border_mm + usable_h / 2.0
            radius = min(usable_w, usable_h) / 2.0
            if radius > 0:
                rr = np.sqrt((X - cx) ** 2 + (Y - cy) ** 2) / radius
                dome = np.clip(1.0 - rr * rr, 0.0, 1.0) * geo.dome_mm
                z_top = z_top + dome
        top = np.stack([X, Y, z_top], axis=-1)
        bottom = np.stack([X, Y, np.zeros_like(z_top)], axis=-1)
        return top, bottom

    # curved / cylindrical — wrap around Y axis with radius R.
    R = max(10.0, geo.curve_radius_mm)
    theta_span = usable_w / R  # arc length / radius
    if geo.mode == "cylindrical":
        theta_span = 2 * np.pi
    thetas = np.linspace(-theta_span / 2, theta_span / 2, w)
    Y, T = np.meshgrid(ys, thetas, indexing="ij")

    # Top surface pushes outward by z_top, bottom sits at radius R.
    r_top = R + z_top
    top = np.stack([
        r_top * np.sin(T),
        Y,
        r_top * np.cos(T) - R,  # offset so back sits at z = 0
    ], axis=-1)
    bottom = np.stack([
        R * np.sin(T),
        Y,
        R * np.cos(T) - R,
    ], axis=-1)
    return top, bottom


def _disc_valid_mask(h: int, w: int, geo: GeometrySpec) -> np.ndarray:
    """For disc mode, returns a (h, w) bool mask of vertices that lie
    inside the inscribed circle. The circle is centered on the usable
    area and uses the smaller of usable_w/usable_h as the diameter."""
    usable_w = geo.width_mm - 2 * geo.border_mm
    usable_h = geo.height_mm - 2 * geo.border_mm
    cx = geo.border_mm + usable_w / 2.0
    cy = geo.border_mm + usable_h / 2.0
    radius = min(usable_w, usable_h) / 2.0
    xs = np.linspace(geo.border_mm, geo.border_mm + usable_w, w)
    ys = np.linspace(geo.border_mm, geo.border_mm + usable_h, h)
    X, Y = np.meshgrid(xs, ys)
    return ((X - cx) ** 2 + (Y - cy) ** 2) <= (radius * radius)


def _build_mesh(
    layer_map: np.ndarray,
    layer_height_mm: float,
    geo: GeometrySpec,
    base_min_layers: int = 1,
) -> Tuple[np.ndarray, np.ndarray]:
    """Return vertices (N, 3) and faces (M, 3) as int32 indices."""
    top, bottom = _build_vertices(layer_map, layer_height_mm, geo, base_min_layers)
    h, w, _ = top.shape

    verts_top = top.reshape(-1, 3)
    verts_bot = bottom.reshape(-1, 3)
    vertices = np.concatenate([verts_top, verts_bot], axis=0)
    top_offset = 0
    bot_offset = verts_top.shape[0]

    def ti(y, x):
        return top_offset + y * w + x

    def bi(y, x):
        return bot_offset + y * w + x

    # For disc mode, only emit triangles where ALL 4 cell corners are
    # inside the circular mask. The side walls then trace the boundary
    # of that masked region instead of the rectangular outline.
    if geo.mode == "disc":
        valid = _disc_valid_mask(h, w, geo)
        cell_ok = (
            valid[:-1, :-1] & valid[:-1, 1:] & valid[1:, :-1] & valid[1:, 1:]
        )
    else:
        cell_ok = np.ones((h - 1, w - 1), dtype=bool)

    faces: List[Tuple[int, int, int]] = []

    if geo.mode in ("flat",):
        # Greedy-meshed top + 2-triangle flat bottom: massive reduction
        # for lithophanes with large flat regions (sky, background, etc.).
        # The mesh remains watertight for slicing purposes (slicers do
        # 2D plane intersections; T-junctions don't affect contours).
        z_top_vert = top[..., 2]  # (h, w)
        for (r0, c0, r1, c1, is_flat) in _greedy_top_rects(z_top_vert, cell_ok):
            if is_flat:
                a = ti(r0, c0)
                b = ti(r0, c1 + 1)
                c = ti(r1 + 1, c1 + 1)
                d = ti(r1 + 1, c0)
                faces.append((a, b, c))
                faces.append((a, c, d))
            else:
                # Single non-flat cell — keep its 4-corner interpolation.
                a = ti(r0, c0)
                b = ti(r0, c0 + 1)
                c = ti(r0 + 1, c0 + 1)
                d = ti(r0 + 1, c0)
                faces.append((a, b, c))
                faces.append((a, c, d))
        # Flat bottom: 2 triangles using the 4 perimeter corners — the
        # interior bottom-grid vertices still exist (the side walls need
        # them) but are not referenced by any bottom face.
        a = bi(0, 0)
        b = bi(0, w - 1)
        c = bi(h - 1, w - 1)
        d = bi(h - 1, 0)
        # Reverse winding (normal points down).
        faces.append((a, c, b))
        faces.append((a, d, c))
    else:
        # Curved / cylindrical / disc: keep the per-cell tessellation
        # because greedy merging doesn't help (curved surface has
        # ~no equal-z neighbours).
        for y in range(h - 1):
            for x in range(w - 1):
                if not cell_ok[y, x]:
                    continue
                a = ti(y, x)
                b = ti(y, x + 1)
                c = ti(y + 1, x + 1)
                d = ti(y + 1, x)
                faces.append((a, b, c))
                faces.append((a, c, d))
        for y in range(h - 1):
            for x in range(w - 1):
                if not cell_ok[y, x]:
                    continue
                a = bi(y, x)
                b = bi(y, x + 1)
                c = bi(y + 1, x + 1)
                d = bi(y + 1, x)
                faces.append((a, c, b))
                faces.append((a, d, c))

    if geo.mode == "disc":
        # Walk every cell-edge where one neighbour is invalid (or off-grid)
        # and add a side wall there. Each edge is parameterised by its
        # two endpoints (vertex indices in the top/bottom grid).
        def add_side_wall(p0, p1, outward_right):
            """Add two triangles forming a quad between top edge (p0,p1)
            and the corresponding bottom edge. Wind so the normal points
            outward."""
            t0, t1 = ti(*p0), ti(*p1)
            b0, b1 = bi(*p0), bi(*p1)
            if outward_right:
                faces.append((t0, b0, b1))
                faces.append((t0, b1, t1))
            else:
                faces.append((t0, b1, b0))
                faces.append((t0, t1, b1))

        for y in range(h - 1):
            for x in range(w - 1):
                if not cell_ok[y, x]:
                    continue
                # Top edge (y, x)-(y, x+1): outside if cell above missing
                if y == 0 or not cell_ok[y - 1, x]:
                    add_side_wall((y, x + 1), (y, x), outward_right=True)
                # Bottom edge (y+1, x)-(y+1, x+1): outside if cell below missing
                if y == h - 2 or not cell_ok[y + 1, x]:
                    add_side_wall((y + 1, x), (y + 1, x + 1), outward_right=True)
                # Left edge (y, x)-(y+1, x): outside if cell left missing
                if x == 0 or not cell_ok[y, x - 1]:
                    add_side_wall((y, x), (y + 1, x), outward_right=True)
                # Right edge (y, x+1)-(y+1, x+1): outside if cell right missing
                if x == w - 2 or not cell_ok[y, x + 1]:
                    add_side_wall((y + 1, x + 1), (y, x + 1), outward_right=True)

        faces_arr = np.array(faces, dtype=np.int32)
        return vertices, faces_arr

    # Four side walls for flat/curved/cylindrical rectangle
    for x in range(w - 1):
        # y=0 edge
        a, b = ti(0, x), ti(0, x + 1)
        c, d = bi(0, x + 1), bi(0, x)
        faces.append((a, d, c))
        faces.append((a, c, b))
        # y=h-1 edge
        a, b = ti(h - 1, x + 1), ti(h - 1, x)
        c, d = bi(h - 1, x), bi(h - 1, x + 1)
        faces.append((a, d, c))
        faces.append((a, c, b))

    for y in range(h - 1):
        # x=0 edge
        a, b = ti(y, 0), ti(y + 1, 0)
        c, d = bi(y + 1, 0), bi(y, 0)
        faces.append((a, b, c))
        faces.append((a, c, d))
        # x=w-1 edge
        a, b = ti(y + 1, w - 1), ti(y, w - 1)
        c, d = bi(y, w - 1), bi(y + 1, w - 1)
        faces.append((a, b, c))
        faces.append((a, c, d))

    faces_arr = np.array(faces, dtype=np.int32)
    return vertices, faces_arr


